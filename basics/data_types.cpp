#include <iostream>
using namespace std;

// Program to understand different data types in C++

int main()
{
    // Integer data type (stores whole numbers)
    int age = 20;
    cout << "Integer: " << age << endl;
    
    // Float data type (stores decimal numbers - 7 decimal places)
    float price = 19.99f;
    cout << "Float: " << price << endl;
    
    // Double data type (stores decimal numbers - 15 decimal places)
    double pi = 3.141592653589793;
    cout << "Double: " << pi << endl;
    
    // Character data type (stores single character)
    char grade = 'A';
    cout << "Character: " << grade << endl;
    
    // Boolean data type (stores true or false)
    bool isStudent = true;
    cout << "Boolean: " << isStudent << endl;
    
    return 0;
}

// Output:
// Integer: 20
// Float: 19.99
// Double: 3.14159
// Character: A
// Boolean: 1
