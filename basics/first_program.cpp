#include <iostream>
using namespace std;

// Basic program to understand how to print output in C++
// cout is used to display output on the console

int main()
{
    // Print using endl (end line - adds new line)
    cout << "Hello everyone" << endl;
    
    // Print using \n (newline character)
    cout << "My name is Prince" << "\n";
    
    return 0;  // Return 0 to indicate successful program execution
}

// Output:
// Hello everyone
// My name is Prince
